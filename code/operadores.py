# Operadores

# Aritméticos --------------------------

# Suma
suma = 10 + 3
print( suma )

# Resta
resta = 10 - 3
print( resta )

# Multiplicación
multiplicacion = 10 * 3
print( multiplicacion )

# División real
division = 10 / 3
print( division )

# Resto de división entera
resto = 10 % 3
print( resto )

# Cociente de división entera
cociente = 10 // 3
print( cociente )

# Potencia
potencia = 10 ** 3
print( potencia )

print( "" )

# Comparación --------------------------

