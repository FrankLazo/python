# Tipos de datos

# Enteros
print( type( 0 ) )
print( type( 37 ) )
print( type( -37 ) )
print( "" )

# Coma flotante
print( type( 12.0 ) )
print( type( 120.50 ) )
print( type( -120.50 ) )
print( "" )

# Complejos
print( type( 12 + 5j ) )
print( type( 12 - 5j ) )
print( type( 12 + 0j ) )
print( type( 0 + 0j ) )
print( type( 0j ) )
print( "" )

# Textos
print( type( 'Testeo #1' ) )
print( type( "Testeo #2" ) )
print( type( '''Testeo #3''' ) )
print( "" )

# Booleanos
print( type( True ) )
print( type( False ) )
